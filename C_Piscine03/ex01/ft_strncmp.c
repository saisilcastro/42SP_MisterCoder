int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	pos;

	pos = 0;
	while (*s1 && *s2 && *s1 == *s2 && pos < n)
	{
		s1++;
		s2++;
		if (!*s1 && *s2)
			return (1);
		if (!*s2 && *s1)
			return (-1);
		pos++;
	}
	return (0);
}
